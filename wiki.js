const goHandler = () => {
  goes.forEach((g) => {
    g.addEventListener("click", (event) => {
      const name = event.target.dataset.name;
      localStorage.setItem("godata", JSON.stringify(godata[name]));
      location.assign("./wiki.html");
    });
  });
};
const lHandler = () => {
  setTimeout(() => {
    const data = JSON.parse(localStorage.getItem("godata"));
    const k = {
      en: ["Chracters", "weapon", "dark mod", "language"],
      fa: ["شخصیت ها", "تسلیحات", "حالت تاریک", "زبان"],
    };
    const ise = JSON.parse(localStorage.getItem("ise"));
    if (!ise) {
      const phs = document.querySelectorAll(".ph");
      phs[0].innerHTML = k.fa[0];
      phs[1].innerHTML = k.fa[1];
      phs[2].innerHTML = k.fa[2];
      phs[3].innerHTML = k.fa[3];
      df.innerHTML = "تاریک";
      lf.innerHTML = "روشن";
      ef.innerHTML = "انگلیسی";
      ff.innerHTML = "فارسی";
      wikiTitr.innerHTML = data[2];
    } else {
      const phs = document.querySelectorAll(".ph");
      phs[0].innerHTML = k.en[0];
      phs[1].innerHTML = k.en[1];
      phs[2].innerHTML = k.en[2];
      phs[3].innerHTML = k.en[3];
      df.innerHTML = "dark";
      lf.innerHTML = "light";
      ef.innerHTML = "EN";
      ff.innerHTML = "FA";
      wikiTitr.innerHTML = data[1];
    }
  }, 400);
};
